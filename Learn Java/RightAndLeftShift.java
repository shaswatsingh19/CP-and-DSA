public class RightAndLeftShift {
    public static void main(String[] args) {
        int a = 5;
        // Right shift means floor divide by 2
        System.out.println("The right shift of "+a +" is");
        System.out.println(a>>1);

        
        System.out.println("The left shift of "+a +" is");
        System.out.println(a<<1);

    }
}
